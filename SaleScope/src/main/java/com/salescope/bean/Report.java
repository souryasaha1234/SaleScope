package com.salescope.bean;

public class Report {
	private String pdtSelect;
	private String salesId;
	
	private String costPrice;
	private String sellPrice;
	private String purchaseQty;
	private String sellQty;
	
	private String totalPurchase;
	private String totalSell;
	private String netProfit;
	
	private String purchaseDate;
	private String sellDate;
	
	
	public String getPdtSelect() {
		return pdtSelect;
	}
	public void setPdtSelect(String pdtSelect) {
		this.pdtSelect = pdtSelect;
	}
	public String getCostPrice() {
		return costPrice;
	}
	public void setCostPrice(String costPrice) {
		this.costPrice = costPrice;
	}
	public String getSellPrice() {
		return sellPrice;
	}
	public void setSellPrice(String sellPrice) {
		this.sellPrice = sellPrice;
	}
	public String getPurchaseQty() {
		return purchaseQty;
	}
	public void setPurchaseQty(String purchaseQty) {
		this.purchaseQty = purchaseQty;
	}
	public String getSellQty() {
		return sellQty;
	}
	public void setSellQty(String sellQty) {
		this.sellQty = sellQty;
	}
	public String getTotalPurchase() {
		return totalPurchase;
	}
	public void setTotalPurchase(String totalPurchase) {
		this.totalPurchase = totalPurchase;
	}
	public String getTotalSell() {
		return totalSell;
	}
	public void setTotalSell(String totalSell) {
		this.totalSell = totalSell;
	}
	public String getNetProfit() {
		return netProfit;
	}
	public void setNetProfit(String netProfit) {
		this.netProfit = netProfit;
	}
	public String getPurchaseDate() {
		return purchaseDate;
	}
	public void setPurchaseDate(String purchaseDate) {
		this.purchaseDate = purchaseDate;
	}
	public String getSellDate() {
		return sellDate;
	}
	public void setSellDate(String sellDate) {
		this.sellDate = sellDate;
	}
	public String getSalesId() {
		return salesId;
	}
	public void setSalesId(String salesId) {
		this.salesId = salesId;
	}
}
